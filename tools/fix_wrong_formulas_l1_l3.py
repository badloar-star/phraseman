def replace_wrong_in_lesson(c: str, lesson: int, idx: int, new_ru: str, new_uk: str) -> str:
    anchor = f"\n  {lesson}: {{"
    start = c.find(anchor)
    nxt = c.find("\n  " + str(lesson + 1) + ": {", start + 1)
    block = c[start:nxt]
    m = re.search(rf"(\n    {idx}: \{{)([\s\S]*?)(\n    \}},)", block)
    if not m:
        raise SystemExit(f"card L{lesson} p{idx} not found")
    inner = m.group(2)

    def repl_ru(mo: re.Match) -> str:
        q = mo.group(1)
        s = new_ru.replace("\\", "\\\\")
        if q == '"':
            s = s.replace('"', '\\"')
        elif q == "'":
            s = s.replace("'", "\\'")
        else:
            s = s.replace("`", "\\`")
        return f"wrongRu: {q}{s}{q},"

    def repl_uk(mo: re.Match) -> str:
        q = mo.group(1)
        s = new_uk.replace("\\", "\\\\")
        if q == '"':
            s = s.replace('"', '\\"')
        elif q == "'":
            s = s.replace("'", "\\'")
        else:
            s = s.replace("`", "\\`")
        return f"wrongUk: {q}{s}{q},"

    new_inner = re.sub(
        r"wrongRu:\s*([`'\"])[\s\S]*?\1,",
        repl_ru,
        inner,
        count=1,
    )
    new_inner = re.sub(
        r"wrongUk:\s*([`'\"])[\s\S]*?\1,",
        repl_uk,
        new_inner,
        count=1,
    )

    new_block = m.group(1) + new_inner + m.group(3)
    g0 = start + m.start(0)
    g1 = start + m.end(0)
    return c[:g0] + new_block + c[g1:]


