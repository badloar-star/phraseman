#!/bin/bash

# Список явно русских слов (имеют украинские эквиваленты)
# Русское -> Украинское
declare -A russian_to_ukrainian=(
    ["красивый"]="гарний"
    ["красивая"]="гарна"
    ["красивое"]="гарне"
    ["маленький"]="малий"
    ["маленькая"]="мала"
    ["маленькое"]="мале"
    ["большой"]="великий"
    ["большая"]="велика"
    ["большое"]="велике"
    ["старый"]="старий"
    ["старая"]="стара"
    ["старое"]="старе"
    ["новый"]="новий"
    ["новая"]="нова"
    ["новое"]="нове"
    ["быстрый"]="швидкий"
    ["быстрая"]="швидка"
    ["быстрое"]="швидке"
    ["медленный"]="повільний"
    ["медленная"]="повільна"
    ["медленное"]="повільне"
    ["интересный"]="цікавий"
    ["интересная"]="цікава"
    ["интересное"]="цікаве"
    ["скучный"]="нудний"
    ["скучная"]="нудна"
    ["скучное"]="нудне"
    ["холодный"]="холодний"
    ["холодная"]="холодна"
    ["холодное"]="холодне"
    ["горячий"]="гарячий"
    ["горячая"]="гаряча"
    ["горячее"]="гаряче"
    ["мокрый"]="мокрий"
    ["мокрая"]="мокра"
    ["мокрое"]="мокре"
    ["сухой"]="сухий"
    ["сухая"]="суха"
    ["сухое"]="сухе"
    ["грязный"]="брудний"
    ["грязная"]="бруна"
    ["грязное"]="бруне"
    ["чистый"]="чистий"
    ["чистая"]="чиста"
    ["чистое"]="чисте"
    ["умный"]="розумний"
    ["умная"]="розумна"
    ["умное"]="розумне"
)

errors=0

for word in "${!russian_to_ukrainian[@]}"; do
    count=$(grep -c "ukrainian:.*\b$word\b" app/lesson_data_9_16.ts 2>/dev/null || echo 0)
    if [ "$count" -gt 0 ]; then
        echo "Found Russian word '$word' in $count lines"
        grep -n "ukrainian:.*\b$word\b" app/lesson_data_9_16.ts | head -3
        errors=$((errors + count))
    fi
done

echo ""
echo "Total Russian words found: $errors"

