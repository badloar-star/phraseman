// ── Аккордеон ─────────────────────────────────────────────────────────────────
function AccordionItem({ item, t, f, isUK }: { item: HelpItem; t: any; f: any; isUK: boolean }) {
  const [open, setOpen] = useState(false);
  const q = isUK ? item.qUK : item.qRU;
  const a = isUK ? item.aUK : item.aRU;

  return (
    <TouchableOpacity
      onPress={() => {
        LayoutAnimation.configureNext(LayoutAnimation.Presets.easeInEaseOut);
        setOpen(v => !v);
      }}
      activeOpacity={0.8}
      style={{ borderBottomWidth: 0.5, borderBottomColor: t.border, paddingVertical: 14, paddingHorizontal: 16 }}
    >
      <View style={{ flexDirection: 'row', alignItems: 'center', gap: 10 }}>
        <Ionicons name={open ? 'chevron-down' : 'chevron-forward'} size={16} color={t.textMuted} />
        <Text style={{ flex: 1, color: t.textPrimary, fontSize: f.body, fontWeight: '500', lineHeight: f.body + 7 }}>
          {q}
        </Text>
      </View>
      {open && (
        <Text style={{ color: t.textSecond, fontSize: f.sub, lineHeight: f.sub + 8, marginTop: 10, marginLeft: 26 }}>
          {a}
        </Text>
      )}
    </TouchableOpacity>
  );
}

