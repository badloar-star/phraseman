const m = (questionId, lessonId, skillTag, stemRu, stemUk, stemEs, key, distractors, eRu, eUk, eEs, difficulty = 2, reviewerFlag = null) => ({
  questionId,
  lessonId,
  skill_tag: skillTag,
  stem_ru: stemRu,
  stem_uk: stemUk,
  stem_es: stemEs,
  type: 'MCQ',
  key,
  distractors,
  post_explanation_ru: eRu,
  post_explanation_uk: eUk,
  post_explanation_es: eEs,
  difficulty,
  reviewer_flag: reviewerFlag,
});

/** rows: [stemRu, stemUk, stemEs, key, [d1,d2,d3], eRu, eUk, eEs, difficulty?, flag?] */
function block(lessonId, skillTag, rows) {
  return rows.map((row, i) => {
    const id = `es-l${lessonId}-${String(i + 1).padStart(2, '0')}`;
    const diff = row.length > 8 ? row[8] : 2;
    const flag = row.length > 9 ? row[9] : null;
    return m(id, lessonId, skillTag, row[0], row[1], row[2], row[3], row[4], row[5], row[6], row[7], diff, flag);
  });
}

