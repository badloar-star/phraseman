const fs = require('fs');
let content = fs.readFileSync('app/quiz_data.ts', 'utf8');
const hasCRLF = content.includes('\r\n');
if (hasCRLF) content = content.replace(/\r\n/g, '\n');

let count = 0;
function rep(old, neu) {
  if (!content.includes(old)) {
    console.error('NOT FOUND:', old.substring(0, 80));
    return false;
  }
  content = content.replace(old, neu);
  count++;
  return true;
}

// Helper: find explanationsUK block for an entry containing a specific Russian phrase
function findAndReplaceUK(ruPhrase, newUKLines) {
  // Find the entry by ru phrase
  const ruIdx = content.indexOf("ru: '" + ruPhrase + "'");
  if (ruIdx === -1) {
    console.error('Entry not found for ru:', ruPhrase);
    return;
  }

  // Find explanationsUK: [ after this position
  const ukStart = content.indexOf('explanationsUK: [', ruIdx);
  if (ukStart === -1 || ukStart - ruIdx > 2000) {
    console.error('explanationsUK not found near:', ruPhrase);
    return;
  }

  // Find the closing ]
  const ukArrayStart = ukStart + 'explanationsUK: ['.length;
  const ukEnd = content.indexOf('\n    ],', ukArrayStart);
  if (ukEnd === -1) {
    console.error('Closing ] not found for:', ruPhrase);
    return;
  }

  const oldBlock = content.substring(ukStart, ukEnd + '\n    ],'.length);
  const newBlock = 'explanationsUK: [\n' + newUKLines.map(l => "      '" + l.replace(/'/g, "\\'") + "',").join('\n') + '\n    ],';

  content = content.replace(oldBlock, newBlock);
  count++;
  console.log('Fixed:', ruPhrase);
}

