function escapeTsSingleQuoted(s) {
  return s.replace(/\\/g, "\\\\").replace(/'/g, "\\'");
}

const raw = fs.readFileSync(tsPath, "utf8");
if (/\r?\n    spanish: '/.test(raw)) {
  console.error("lesson_data_9_16.ts already contains spanish fields; abort.");
  process.exit(1);
}

const nl = raw.includes("\r\n") ? "\r\n" : "\n";
let idx = 0;

const chunks = raw.split(/(?=\r?\n  \{\s*\r?\n    id: 'lesson\d+_phrase_\d+')/);
const out =
  chunks
    .map((ch) => {
      if (!/id: 'lesson\d+_phrase_\d+'/.test(ch)) return ch;
      const spEsc = escapeTsSingleQuoted(ALL[idx++]);
      const replaced = ch.replace(
        /(\r?\n    ukrainian: '((?:[^'\\]|\\.)*)',)/,
        (_, ukrLine) => `${ukrLine}${nl}    spanish: '${spEsc}',`,
      );
      if (replaced === ch) {
        console.error("Could not insert spanish near ukrainian in phrase block index", idx - 1);
        process.exit(1);
      }
      return replaced;
    })
    .join("");

if (idx !== 400) {
  console.error("Phrase block count mismatch:", idx);
  process.exit(1);
}

